package com.qualitymap.vo;

import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name="network_type")
public class NetworkType {

	@Id
	private Integer id;
	private String month;
	private String groupid;
	private String province;
	private Integer new_user_num;

	private Integer accumulativ_num;
	private String net_type;
	private Double proportion;

	
	public NetworkType(){
		
	}
	
	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getMonth() {
		return month;
	}

	public void setMonth(String month) {
		this.month = month;
	}

	public String getGroupid() {
		return groupid;
	}

	public void setGroupid(String groupid) {
		this.groupid = groupid;
	}

	public String getProvince() {
		return province;
	}

	public void setProvince(String province) {
		this.province = province;
	}

	public Integer getNew_user_num() {
		return new_user_num;
	}

	public void setNew_user_num(Integer new_user_num) {
		this.new_user_num = new_user_num;
	}


	public Integer getAccumulativ_num() {
		return accumulativ_num;
	}

	public void setAccumulativ_num(Integer accumulativ_num) {
		this.accumulativ_num = accumulativ_num;
	}

	public String getNet_type() {
		return net_type;
	}

	public void setNet_type(String net_type) {
		this.net_type = net_type;
	}

	public Double getProportion() {
		return proportion;
	}

	public void setProportion(Double proportion) {
		this.proportion = proportion;
	}

}
